import java.util.Scanner;

public class ForLoop {

   public static void main(String[] args) {
       
     // Use a for loop to print out the odd numbers from 1 to 10.
     for (int i = 1; i <= 10; i++) {
         
       if (i % 2 == 1) {
         System.out.println(i);
       }
         
     }
     
   }
    
}
